package puzzle.hill_climbing;

public class Pasos {

	public int numero;
	public String movimiento;
	
	public Pasos(int num, String mov){
		this.numero=num;
		this.movimiento=mov;
	}

	/**
	 * @return the numero
	 */
	public int getNumero() {
		return numero;
	}

	/**
	 * @return the movimiento
	 */
	public String getMovimiento() {
		return movimiento;
	}
	
	
	
}
